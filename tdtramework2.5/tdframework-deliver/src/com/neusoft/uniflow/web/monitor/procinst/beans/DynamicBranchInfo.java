package com.neusoft.uniflow.web.monitor.procinst.beans;

public class DynamicBranchInfo {

	private String branchInstId;
	private String branchDefId;
	private String branchInstTitle;
	private String info;
	private String otherInfo;
	public String getBranchInstId() {
		return branchInstId;
	}
	public void setBranchInstId(String branchInstId) {
		this.branchInstId = branchInstId;
	}
	public String getBranchDefId() {
		return branchDefId;
	}
	public void setBranchDefId(String branchDefId) {
		this.branchDefId = branchDefId;
	}
	public String getBranchInstTitle() {
		return branchInstTitle;
	}
	public void setBranchInstTitle(String branchInstTitle) {
		this.branchInstTitle = branchInstTitle;
	}
	public String getInfo() {
		return info;
	}
	public void setInfo(String info) {
		this.info = info;
	}
	public String getOtherInfo() {
		return otherInfo;
	}
	public void setOtherInfo(String otherInfo) {
		this.otherInfo = otherInfo;
	}
}
